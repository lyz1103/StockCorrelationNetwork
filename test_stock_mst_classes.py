#==================6. Test Optimizer.backtrackingSelection consistency==================
#check that the backtracked selection matches DP value for another small tree (star).
def compute_pairwise_sum_from_dist_matrix(selected, dist_matrix):
    sel = list(selected)
    total = 0.0
    for i in range(len(sel)):
        for j in range(i+1, len(sel)):
            u, v = sel[i], sel[j]
            total += dist_matrix[u][v]
    return total

def test_optimizer_star_backtracking():
    mst_obj = stockMST.__new__(stockMST)
    mst_obj.mst = build_star_mst()

    parent, children, dist_to_parent, distance, depth = mst_obj.generateRootedTree("O")
    opt = Optimizer(parent, children, dist_to_parent, root="O")

    # dist matrix consistent with star:
    # O-A=1, O-B=2, O-C=3
    # leaf-leaf distances are sums via O
    dist_matrix = {
        "O": {"O": 0, "A": 1, "B": 2, "C": 3},
        "A": {"O": 1, "A": 0, "B": 3, "C": 4},
        "B": {"O": 2, "A": 3, "B": 0, "C": 5},
        "C": {"O": 3, "A": 4, "B": 5, "C": 0},
    }

    K = 3
    res = opt.GetminCorrPortfolio(K)
    value = res["maximum pairwise distance sum"]
    selected = res["selected K tickers"]

    assert len(selected) == 3
    brute = compute_pairwise_sum_from_dist_matrix(selected, dist_matrix)
    # DP value should match true pairwise sum
    assert math.isclose(value, brute)


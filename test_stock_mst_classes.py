#==================1. Test StockInfo correlation → distance (no Yahoo call)===========
# inject a fake price_df directly so nothing is downloaded.
def test_stockinfo_corr_to_distance_local():
    # Create a tiny synthetic price DataFrame
    dates = pd.date_range("2020-01-01", periods=5, freq="D")
    prices = pd.DataFrame(
        {
            "AAA": [100, 101, 102, 103, 104],
            "BBB": [50, 50.5, 51, 51.5, 52],  # almost perfectly correlated with AAA
            "CCC": [10, 9, 11, 8, 12],        # more noisy
        },
        index=dates,
    )

    si = StockInfo(["AAA", "BBB", "CCC"], "2020-01-01", "2020-01-10")
    si.price_df = prices  # bypass yfinance

    corr = si.getCorrMatrix()
    dist_df = si.CorrToDistance()

    # Correlation diagonal = 1 → distance 0
    for t in ["AAA", "BBB", "CCC"]:
        assert math.isclose(dist_df.loc[t, t], 0.0)

    # Higher correlation → smaller distance
    rho_AB = corr.loc["AAA", "BBB"]
    rho_AC = corr.loc["AAA", "CCC"]
    assert abs(rho_AB) > abs(rho_AC)  # AB is more correlated than AC

    d_AB = dist_df.loc["AAA", "BBB"]
    d_AC = dist_df.loc["AAA", "CCC"]
    assert d_AB < d_AC

#==================2. Test stockMST.generateMST on a simple triangle===========
def test_stockmst_generateMST_triangle():
    # Distances: AB=1, BC=2, AC=10 → MST should keep AB and BC (total=3)
    tickers = ["A", "B", "C"]
    dist = pd.DataFrame(
        [
            [0.0, 1.0, 10.0],
            [1.0, 0.0, 2.0],
            [10.0, 2.0, 0.0],
        ],
        index=tickers,
        columns=tickers,
    )

    mst_obj = stockMST(dist)
    min_weight, mst = mst_obj.minweight, mst_obj.mst

    # Check total MST weight
    assert math.isclose(min_weight, 1.0 + 2.0)

    # Graph should have undirected edges AB and BC
    assert set(mst.keys()) == {"A", "B", "C"}
    assert mst["A"] == {"B": 1.0}
    assert mst["B"]["A"] == 1.0
    assert mst["B"]["C"] == 2.0
    assert mst["C"]["B"] == 2.0

